private void diseñarPanelCierre() {
        // 1. Limpiamos el panel principal generado por NetBeans
        jPanel1.removeAll();
        jPanel1.setLayout(null);

        // 2. Creamos un contenedor centrado simulando la ventana de tu captura
        JPanel pnlVentana = new JPanel(null);
        pnlVentana.setBounds(50, 50, 520, 280);
        pnlVentana.setBorder(BorderFactory.createEtchedBorder());

        // --- PANEL DE ADVERTENCIA ---
        JPanel pnlAdver = new JPanel(null);
        pnlAdver.setBorder(BorderFactory.createEtchedBorder());
        pnlAdver.setBounds(15, 15, 490, 100);

        // Signos de exclamación gigantes
        JLabel lblExclamacionIzq = new JLabel("¡", SwingUtilities.CENTER);
        lblExclamacionIzq.setFont(new Font("Arial", Font.BOLD, 60));
        lblExclamacionIzq.setBounds(10, 10, 50, 80);
        pnlAdver.add(lblExclamacionIzq);

        JLabel lblExclamacionDer = new JLabel("!", SwingUtilities.CENTER);
        lblExclamacionDer.setFont(new Font("Arial", Font.BOLD, 60));
        lblExclamacionDer.setBounds(430, 10, 50, 80);
        pnlAdver.add(lblExclamacionDer);

        // Textos (Corregí un pequeño error gramatical de la imagen original)
        JLabel lblTexto1 = new JLabel("Este proceso se encarga de cerrar el mes de proceso", SwingUtilities.CENTER);
        lblTexto1.setFont(new Font("Segoe UI", Font.BOLD, 14));
        lblTexto1.setBounds(60, 25, 370, 20);
        pnlAdver.add(lblTexto1);

        JLabel lblTexto2 = new JLabel("por lo cual deberá estar seguro de ello.", SwingUtilities.CENTER);
        lblTexto2.setFont(new Font("Segoe UI", Font.BOLD, 14));
        lblTexto2.setBounds(60, 50, 370, 20);
        pnlAdver.add(lblTexto2);

        pnlVentana.add(pnlAdver);

        // --- PANEL DE SELECCIÓN ---
        JPanel pnlSel = new JPanel(null);
        pnlSel.setBorder(BorderFactory.createEtchedBorder());
        pnlSel.setBounds(15, 125, 490, 55);

        pnlSel.add(new JLabel("Compañía")).setBounds(20, 15, 70, 25);
        pnlSel.add(new JComboBox<>(new String[]{"12"})).setBounds(90, 15, 60, 25);

        pnlSel.add(new JLabel("Año")).setBounds(180, 15, 40, 25);
        pnlSel.add(new JComboBox<>(new String[]{"2026"})).setBounds(220, 15, 70, 25);

        pnlSel.add(new JLabel("Período")).setBounds(320, 15, 50, 25);
        pnlSel.add(new JComboBox<>(new String[]{"5"})).setBounds(380, 15, 60, 25);

        pnlVentana.add(pnlSel);

        // --- BOTONES ---
        JButton btnAceptar = new JButton("Aceptar");
        btnAceptar.setBounds(130, 200, 110, 45);
        
        JButton btnSalir = new JButton("Salir");
        btnSalir.setBounds(280, 200, 110, 45);

        pnlVentana.add(btnAceptar);
        pnlVentana.add(btnSalir);

        // --- EVENTOS ---
        btnAceptar.addActionListener(e -> {
            int resp = JOptionPane.showConfirmDialog(jPanel1, 
                "¿Está completamente seguro de realizar el Cierre de Mes?", 
                "Confirmación Crítica", 
                JOptionPane.YES_NO_OPTION, 
                JOptionPane.WARNING_MESSAGE);
                
            if(resp == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(jPanel1, "Procesando... El mes ha sido cerrado exitosamente.");
            }
        });

        btnSalir.addActionListener(e -> {
             JOptionPane.showMessageDialog(jPanel1, "Saliendo del módulo de Cierre de Mes...");
             // Aquí puedes agregar la lógica para cerrar la pestaña de tu menú principal
        });

        // 3. Agregamos el nuevo diseño y refrescamos la pantalla
        jPanel1.add(pnlVentana);
        jPanel1.revalidate();
        jPanel1.repaint();
    }

