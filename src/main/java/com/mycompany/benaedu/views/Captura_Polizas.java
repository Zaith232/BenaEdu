    private void btnDeleteCPolizasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCPolizasActionPerformed
        int fila = tblCPolizas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una póliza para eliminar/cancelar.");
            return;
        }

        String numPoliza = tblCPolizas.getValueAt(fila, 1).toString();
        
        int resp = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar la póliza número: " + numPoliza + "?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
        
        if (resp == JOptionPane.YES_OPTION) {
            try {
                ConDB db = new ConDB();
                Connection con = db.Conectar();
                if (con != null) {
                    // ATENCIÓN: Cambia por tu tabla real
                    String sql = "DELETE FROM tabla_polizas WHERE num_poliza = ?";
                    PreparedStatement ps = con.prepareStatement(sql);
                    ps.setString(1, numPoliza);
                    
                    if (ps.executeUpdate() > 0) {
                        JOptionPane.showMessageDialog(this, "Póliza eliminada correctamente.");
                        cargarTablaPolizas();
                    }
                    ps.close(); db.Cerrar();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar la póliza: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteCPolizasActionPerformed
