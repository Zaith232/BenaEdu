private void cargarTablaPolizas() {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object[][] {}, 
            new String[] {"Compañía", "Núm Póliza", "Tipo", "Fecha", "Descripción", "Cargos", "Créditos", "Diferencia"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        tblCPolizas.setModel(modelo);

        try {
            ConDB db = new ConDB();
            Connection con = db.Conectar();

            if (con != null) {
                // ATENCIÓN: Cambia por tu tabla real de encabezados de póliza
                String sql = "SELECT compania, num_poliza, tipo_poliza, fecha, descripcion, total_cargos, total_creditos, diferencia FROM tabla_polizas";
                PreparedStatement ps = con.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    Object[] fila = new Object[8]; 
                    fila[0] = rs.getString("compania");
                    fila[1] = rs.getString("num_poliza");
                    fila[2] = rs.getString("tipo_poliza");
                    fila[3] = rs.getString("fecha");
                    fila[4] = rs.getString("descripcion");
                    fila[5] = rs.getString("total_cargos");
                    fila[6] = rs.getString("total_creditos");
                    fila[7] = rs.getString("diferencia");
                    modelo.addRow(fila);
                }
                rs.close(); ps.close(); db.Cerrar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar el historial de pólizas: " + e.getMessage());
        }
    }
