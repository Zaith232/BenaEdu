    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
      String correo = txtEmail.getText().trim();

        if (correo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa el correo electrónico.");
            return;
        }

        try {
            ConDB db = new ConDB();
            Connection con = db.Conectar();

            if (con == null) {
                JOptionPane.showMessageDialog(this, "Error de conexión a la base de datos.");
                return;
            }

            // 1. Verificar si el correo existe en la tabla clients
            PreparedStatement psVerificar = con.prepareStatement("SELECT nombre_usuario FROM users WHERE correo = ?");
            psVerificar.setString(1, correo);
            ResultSet rs = psVerificar.executeQuery();

            if (rs.next()) {
                String nombreUsuario = rs.getString("nombre_usuario");

                // 2. Generar una contraseña temporal aleatoria de 8 caracteres
                String claveTemporal = UUID.randomUUID().toString().substring(0, 8);

                // 3. Encriptar la contraseña temporal
                String hashTemporal = BCrypt.hashpw(claveTemporal, BCrypt.gensalt());

                // 4. Actualizar la base de datos (guardar el hash y marcar temporal = 1)
                PreparedStatement psActualizar = con.prepareStatement("UPDATE users SET contrasena = ?, temporal = 1 WHERE correo = ?");
                psActualizar.setString(1, hashTemporal);
                psActualizar.setString(2, correo);
                
                int actualizado = psActualizar.executeUpdate();

                if (actualizado > 0) {
                    // 5. Simular el envío por correo (En un sistema real aquí usarías JavaMail API)
                    JOptionPane.showMessageDialog(this, 
                        "¡Correo enviado con éxito!\n\n" +
                        "(Simulación) Dile al usuario " + nombreUsuario + " que su clave temporal es:\n" + 
                        claveTemporal, 
                        "Recuperación Exitosa", 
                        JOptionPane.INFORMATION_MESSAGE);
                        
                    // Limpiar el campo o regresar al login
                    txtEmail.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Ocurrió un error al intentar actualizar la contraseña.");
                }
                
                psActualizar.close();

            } else {
                JOptionPane.showMessageDialog(this, "No existe ningún usuario registrado con ese correo.");
            }

            rs.close();
            psVerificar.close();
            db.Cerrar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en la base de datos: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSendActionPerformed

