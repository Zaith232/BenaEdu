    private void btnDeleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteUserActionPerformed
       // 1. Verificar si hay una fila seleccionada
        int filaSeleccionada = tblUsers.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona un usuario de la tabla para eliminar.");
            return;
        }

        // 2. Obtener el nombre del usuario seleccionado (columna 0)
        String usuarioAEliminar = tblUsers.getValueAt(filaSeleccionada, 0).toString();

        // 3. Pedir la contraseña del administrador
        javax.swing.JPasswordField pwdField = new javax.swing.JPasswordField();
        int opcion = JOptionPane.showConfirmDialog(
            this,
            pwdField,
            "Ingrese la contraseña de un Administrador para confirmar",
            JOptionPane.OK_CANCEL_OPTION,
            JOptionPane.PLAIN_MESSAGE
        );

        if (opcion == JOptionPane.OK_OPTION) {
            String claveIngresada = new String(pwdField.getPassword()).trim();

            if (claveIngresada.isEmpty()) {
                JOptionPane.showMessageDialog(this, "La contraseña no puede estar vacía.");
                return;
            }

            try {
                ConDB db = new ConDB();
                Connection con = db.Conectar();

                if (con == null) {
                    JOptionPane.showMessageDialog(this, "Error de conexión a la base de datos.");
                    return;
                }

                // 4. Validar que la clave pertenece a un administrador
                PreparedStatement psAdmin = con.prepareStatement("SELECT contrasena FROM users WHERE rol = 'Administrador'");
                ResultSet rsAdmin = psAdmin.executeQuery();
                boolean adminValido = false;

                while (rsAdmin.next()) {
                    if (BCrypt.checkpw(claveIngresada, rsAdmin.getString("contrasena"))) {
                        adminValido = true;
                        break;
                    }
                }

                rsAdmin.close();
                psAdmin.close();

                // 5. Si la clave es válida, eliminamos al usuario
                if (adminValido) {
                    PreparedStatement psDelete = con.prepareStatement("DELETE FROM users WHERE nombre_usuario = ?");
                    psDelete.setString(1, usuarioAEliminar);
                    
                    int eliminado = psDelete.executeUpdate();

                    if (eliminado > 0) {
                        JOptionPane.showMessageDialog(this, "Usuario '" + usuarioAEliminar + "' eliminado correctamente.");
                        cargarUsuarios(); // Recargar la tabla para que desaparezca visualmente
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo eliminar al usuario.");
                    }
                    psDelete.close();
                } else {
                    JOptionPane.showMessageDialog(this, "Contraseña incorrecta o no pertenece a un administrador. Operación cancelada.");
                }

                db.Cerrar();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error en la base de datos: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteUserActionPerformed

