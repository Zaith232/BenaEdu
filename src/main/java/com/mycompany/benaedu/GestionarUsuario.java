    private void cargarUsuarios() {
        // Obtenemos el modelo de la tabla para poder modificar sus filas
        DefaultTableModel modelo = (DefaultTableModel) tblUsers.getModel();
        modelo.setRowCount(0); // Esto limpia la tabla antes de cargar nuevos datos

        try {
            ConDB db = new ConDB();
            Connection con = db.Conectar();

            if (con != null) {
                // Consultamos los datos que nos interesan
                PreparedStatement ps = con.prepareStatement("SELECT nombre_usuario, correo, rol FROM users");
                ResultSet rs = ps.executeQuery();

                // Recorremos los resultados y los agregamos fila por fila
                while (rs.next()) {
                    Object[] fila = new Object[3];
                    fila[0] = rs.getString("nombre_usuario");
                    fila[1] = rs.getString("correo");
                    fila[2] = rs.getString("rol");
                    modelo.addRow(fila);
                }

                rs.close();
                ps.close();
                db.Cerrar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los usuarios: " + e.getMessage());
        }
    }

