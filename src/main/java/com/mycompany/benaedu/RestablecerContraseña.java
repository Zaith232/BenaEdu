    private void btnRestoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestoreActionPerformed
     // Asumiendo que tus campos se llaman txtNuevaClave y txtConfirmarClave (tipo JPasswordField)
        String nuevaClave = new String(txtNewPassword.getPassword()).trim();
        String confirmarClave = new String(txtConfirmPassword.getPassword()).trim();

        if (nuevaClave.isEmpty() || confirmarClave.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
            return;
        }

        if (!nuevaClave.equals(confirmarClave)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden. Intenta de nuevo.");
            return;
        }

        try {
            ConDB db = new ConDB();
            Connection con = db.Conectar();

            if (con == null) {
                JOptionPane.showMessageDialog(this, "Error de conexión a la base de datos.");
                return;
            }

            // 1. Encriptar la nueva contraseña definitiva
            String hashNuevo = BCrypt.hashpw(nuevaClave, BCrypt.gensalt());

            // 2. Actualizar la base de datos: guardamos el nuevo hash y quitamos el estado temporal (temporal = 0)
            // *OJO*: En tu último código de Login usaste la tabla "users", asegúrate de usar el nombre correcto aquí
            String sql = "UPDATE users SET contrasena = ?, temporal = 0 WHERE nombre_usuario = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, hashNuevo);
            ps.setString(2, usuarioGlobal); // Usamos el usuario que recibimos del Login

            int actualizado = ps.executeUpdate();

            if (actualizado > 0) {
                JOptionPane.showMessageDialog(this, "¡Contraseña actualizada con éxito! Por favor, inicia sesión de nuevo.");
                
                // 3. Cerramos esta ventana y devolvemos al usuario al Login
                this.dispose();
                FlatMTMaterialLighterIJTheme.setup();
                Login login = new Login();
                login.setLocationRelativeTo(null);
                login.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Ocurrió un error al actualizar la contraseña.");
            }

            ps.close();
            db.Cerrar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en la base de datos: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRestoreActionPerformed

